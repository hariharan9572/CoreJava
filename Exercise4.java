public class Exercise4 {
    public static void main(Strings[] args) {
        System.out.print("For Loop: ");
        for(int i=1;i<6;i++){
            System.out.print(i+" ");
        }
        
        System.out.print("\nWhile Loop: ");
        int j=1;
        while(j<=5){
            System.out.print(j+" ");
            j++;
        }

        System.out.print("\nDo-While Loop: ");
        int z=1;
        do { 
            System.out.print(z+" ");
            z++;
        } while (z<=5);
    }
}
